package org.project;

/**
 * Interface for Drawable object (any object that will appear on the PApplet Window).
 *
 * @author Buck Sin and Lester Shun
 * @version 1.0
 */
public interface Drawable {

  /**
   * Method stub to draw onto window.
   */
  public void draw();

  /**
   * Method stub to "move" object implementing Drawable.
   */
  public void move();
}
